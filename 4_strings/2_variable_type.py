# type(variable_name) function is useful for knowing the type of the variable we are using at the moment
# note: python is a dynamically typed language
# int : integer
my_int = 2
print(my_int, 'variable type', type(my_int))

# float
my_float = 3.14
print(my_float, 'variable type', type(my_float))

# bool : boolean
my_bool = True
print(my_bool, 'variable type', type(my_bool))

# str : string
my_str = 'Here is my'
print(my_str, 'variable type', type(my_str))
