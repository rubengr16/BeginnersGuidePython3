# string.startswith(substring) tells us if a string starts with the substring or
# string.endswith(substring) tells us if a string ends with the substring, both return a boolean
jon = 'jon'
ygritte = 'ygritte'

print('jon starts with "j": ', jon.startswith('j'))
print('jon starts with "y": ', jon.startswith('y'))
print('ygritte ends with "j": ',ygritte.endswith('j'))
print('ygritte ends with "te": ', ygritte.endswith('te'))