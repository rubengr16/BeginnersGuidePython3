# Can be used to combine boolean expresions together and make complex conditions, returns a boolean value

print('True and True:', True and True)  # and operator evaluates to true if expressions on both sides are True
print('True and False:', True and False)
print('False and False:', False and False)

print('True or True:', True or True)  # or operator evaluates to true if one of the expressions is True
print('True or False:', True or False)
print('False or False:', False or False)

print('not True:', not True)  # not returns the opossite boolean value: False if it is True and True if it is False
print('not False:', not False)