# NoneType has a single value: None
# it is used to create a variable with no initial value for it
none = None  # None assignment

print('none value =', none)
print('none type =', type(none))

# Using is and is not we can ask for the presence of None having as result a boolean

print('none is None?:', none is None)
print('"notNone" is None?:', 'notNone' is None)
print('"notNone" is not None?:', 'notNone' is not None)